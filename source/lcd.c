void LCD_Draw_Circle(uint16_t x, uint16_t y, uint16_t radius, uint16_t color) {

	int16_t x1 = radius-1;
	int16_t y1 = 0;
	int16_t dx = 1;
	int16_t dy = 1;
	int16_t err = dx - (radius << 1);

	while (x1 >= y1) {

		LCD_Draw_Point(x + x1, y + y1, color);
		LCD_Draw_Point(x + y1, y + x1, color);
		LCD_Draw_Point(x - y1, y + x1, color);
		LCD_Draw_Point(x - x1, y + y1, color);
		LCD_Draw_Point(x - x1, y - y1, color);
		LCD_Draw_Point(x - y1, y - x1, color);
		LCD_Draw_Point(x + y1, y - x1, color);
		LCD_Draw_Point(x + x1, y - y1, color);

		if (err <= 0) {

			y1++;
			err += dy;
			dy += 2;

		} else {

			x1--;
			dx += 2;
			err += dx - (radius << 1);
		}
	}
}

