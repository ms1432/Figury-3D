static void Driver_Delay_us(uint32_t xus) {

	for(volatile int j=xus*15; j > 0; j--) // CPU CLK 150MHz
		;
}

static void SPI_Write_Byte(LPSPI_Type *base, uint16_t data) {

	LPSPI_FlushFifo(base, true, true);
	LPSPI_SetFrameSize(base, 8);
	Driver_Delay_us(3);

	while(LPSPI_GetTxFifoCount(base) > 7)  // TX FIFO SIZE
		;
	LPSPI_WriteData(base, data);
	Driver_Delay_us(3);
}

static void SPI_Write_Words(LPSPI_Type *base, uint16_t *data, uint32_t size) {

	LPSPI_FlushFifo(base, true, true);
	LPSPI_SetFrameSize(base, 16);
	Driver_Delay_us(3);

	LPSPI_SetPCSContinous(base, true);
	for(int i=0; i<size; i++) {

		while(LPSPI_GetTxFifoCount(base) > 7) // TX FIFO SIZE
			;
		LPSPI_WriteData(base, data[i]);
	}
	LPSPI_SetPCSContinous(base, false);
	Driver_Delay_us(3);
}

static void LCD_WriteReg(uint8_t Reg) {

	LCD_DC_0;
	SPI_Write_Byte(SPI_base, Reg);
}

static void LCD_WriteData_8Bit(uint8_t Data) {

	LCD_DC_1;
	SPI_Write_Byte(SPI_base, Data);
}

