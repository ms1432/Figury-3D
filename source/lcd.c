void LCD_Draw_Line(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2, uint16_t color) {

	uint8_t  x, y;
	int16_t addx, addy, dx, dy, P;

	dx = abs(x2 - x1);
	dy = abs(y2 - y1);
	x = x1;
	y = y1;
	addx = (x1 > x2) ? -1 : 1;
	addy = (y1 > y2) ? -1 : 1;

	if(dx >= dy) {

		P = 2*dy - dx;
		for(int16_t i=0; i<=dx; i++) {
			LCD_Draw_Point(x, y, color);

			if(P < 0){
				P += 2*dy;
				x += addx;
			}else{
				P += 2*(dy - dx);
				x += addx;
				y += addy;
			}
		}
	} else {

		P = 2*dx - dy;
		for(int16_t i=0; i<=dy; i++) {
			LCD_Draw_Point(x, y, color);
			if(P < 0) {

				P += 2*dx;
				y += addy;
			} else {

				P += 2*(dx - dy);
				x += addx;
				y += addy;
			}
		}
	}
}

