void LCD_Init(LPSPI_Type *base) {

	SPI_base=base;
	linearFrameBuffer=(uint16_t*)frameBuffer;
	//SPI RX Disable
	base->TCR |= LPSPI_TCR_RXMSK_MASK; // RX MASK
	//Hardware reset
	LCD_RST_1;
	Driver_Delay_us(1000);
	LCD_RST_0;
	Driver_Delay_us(1000);
	LCD_RST_1;
	Driver_Delay_us(1000);
	//Set the initialization register
	LCD_InitReg();
	Driver_Delay_us(2000);
	//Turn off sleep mode
	LCD_WriteReg(0x11);
	//Clear the LCD display
	LCD_Clear(0x0000);
	LCD_GramRefresh();
	//Turn on the LCD display
	LCD_WriteReg(0x29);
}

void LCD_GramRefresh () {

	LCD_SetHome();
	LCD_DC_1;
	SPI_Write_Words(SPI_base, linearFrameBuffer, LCD_WIDTH*LCD_HEIGHT);
}

